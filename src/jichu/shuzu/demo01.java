package jichu.shuzu;
public class demo01{
	public static void main(String args[]){
		boolean b = false ;
		int x = 10 ;
		int y = -30 ;
		System.out.println("b = " + b + "， !b = " + !b) ;	// 取反
		System.out.println("x = " + x + "， -x = " + -x) ;	// 使用符号
		System.out.println("y = " + y + "， +y = " + +y) ;	// 使用正号
	}
};