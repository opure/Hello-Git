package jichu.shuzu;

public class demo02{
	public static void main(String args[]){
		int i = 10 ;
		int j = 3;
		System.out.println(i + " + " + j + " = " + (i+j));	// 加法操作
		System.out.println(i + " - " + j + " = " + (i-j)) ;	// 减法操作
		System.out.println(i + " * " + j + " = " + (i*j)) ;	// 乘法操作
		System.out.println(i + " / " + j + " = " + (i/j)) ;	// 加法操作
		System.out.println(i + " % " + j + " = " + (i%j)) ;	// 取模操作
	}
};